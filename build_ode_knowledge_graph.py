def main():
    wb = Workbook()
    ws = wb.active
    ws.title = SHEET_NAME

    ws.merge_cells("A1:O1")
    ws["A1"] = TITLE_TEXT
    ws["A1"].font = TITLE_FONT
    ws["A1"].fill = TITLE_FILL
    ws["A1"].alignment = TITLE_ALIGN

    for idx, col_name in enumerate(COLUMNS, start=1):
        cell = ws.cell(row=2, column=idx, value=col_name)
        cell.font = HEADER_FONT
        cell.fill = HEADER_FILL
        cell.alignment = HEADER_ALIGN

    for r_idx, data in enumerate(rows, start=3):
        fill = ROW_LIGHT_FILL if (r_idx % 2 == 1) else ROW_WHITE_FILL
        for c_idx, value in enumerate(data, start=1):
            cell = ws.cell(row=r_idx, column=c_idx, value=value if value is not None else "")
            cell.font = DATA_FONT
            cell.fill = fill
            cell.alignment = DATA_ALIGN
            cell.border = DATA_BORDER

    ws.row_dimensions[1].height = 30
    ws.row_dimensions[2].height = 20
    for r_idx in range(3, 3 + len(rows)):
        ws.row_dimensions[r_idx].height = 35

    for col in range(1, 16):
        ws.column_dimensions[chr(64 + col)].width = 17.578125

    wb.save(OUT_FILE)
    print(str(OUT_FILE))


if __name__ == "__main__":
    main()
